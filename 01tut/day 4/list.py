my_likes = ["Hacking", "Telegram", "Anime", "Python", "Beans", "Soft Cakes"]
print(my_likes)

# append => adds one item to the list
my_likes.append("Moon cakes")
print(my_likes)

# extend => adds multiple items to the list
my_likes.extend(["New Stuff", "Japan", "Gaming"])
print(my_likes)

# pop
my_likes.remove("Anime")
print(my_likes)

